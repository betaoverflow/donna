class User {
  final String imagePath;
  final String name;
  final String email;
  final String mobile;
  final String about;
  final String address;
  final bool isDarkMode;

  const User({
    required this.imagePath,
    required this.name,
    required this.email,
    required this.mobile,
    required this.about,
    required this.address,
    required this.isDarkMode,
  });
}
